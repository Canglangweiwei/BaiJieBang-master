package com.water.helper.bean;


public class FaChildBean {

    /**
     * name : 0
     * n_sum : 17
     * y_sum : 23
     * z_sum : 40
     */

    private String name;
    private int n_sum;
    private int y_sum;
    private int z_sum;

    public FaChildBean() {
        super();
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getN_sum() {
        return n_sum;
    }

    public void setN_sum(int n_sum) {
        this.n_sum = n_sum;
    }

    public int getY_sum() {
        return y_sum;
    }

    public void setY_sum(int y_sum) {
        this.y_sum = y_sum;
    }

    public int getZ_sum() {
        return z_sum;
    }

    public void setZ_sum(int z_sum) {
        this.z_sum = z_sum;
    }
}
